    private void MenRelCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenRelCliActionPerformed
        // Gerando um Relátorio de clientes
        int confirma = JOptionPane.showConfirmDialog(null, "Confirma a impressão deste relátorio?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            // Imprimindo Relátorio com framework JasperReports
            try {
                // Usando a classe  JasperPrint para  preparar a impressão de um  relátorio
                JasperPrint print;
                print = JasperFillManager.fillReport("C:/Reports/clientes.jasper", null, conexao);
                // A linha abaixo exibe o relátorio atráves da classe  JasperViewer

                JasperViewer.viewReport(print, false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_MenRelCliActionPerformed

