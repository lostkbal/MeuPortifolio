    private void MenServActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenServActionPerformed
        // Gerando um Relátorio de serviços
        int confirma = JOptionPane.showConfirmDialog(null, "Confirma a emissão deste relátorio?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            // Emitindo o Relátorio com framework JasperReports
            try {
                // Usando a classe  JasperPrint para  preparar a emissão de um  relátorio
                JasperPrint print;
                print = JasperFillManager.fillReport("C:/Reports/servico.jasper", null, conexao);
                // A linha abaixo exibe o relátorio atráves da classe  JasperViewer

                JasperViewer.viewReport(print, false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_MenServActionPerformed

